/**
 * @param {number} a
 * @param {number} b
 * @return {number}
 */
function add(a,b) {
  return a + b;
}

/**
 * @param {number} a
 * @param {number} b
 * @return {number}
 */
function subtract(a, b) {
  return a - b;
}

/**
 * @param {number} a
 * @param {number} b
 * @return {number}
 */
function multiply(a, b) {
  return a * b;
}

export {add, subtract, multiply};
